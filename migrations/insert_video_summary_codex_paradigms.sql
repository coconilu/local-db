## 范式 5：搭建 Agent 团队

**核心思想**：复杂任务拆成多个 Agent 同时跑，像派任务给多个员工

**一人公司模式**：单个 Agent 搞不定所有事，多个 Agent 团队才能办成大事。

### 茶饮创新大会案例
- **主 Agent**（项目统筹/领导）：负责读取大会信息、分发任务、核查结果、汇总启动包。
- **子 Agent 1**（海报设计师）：用 Canva 做大会邀请函海报。
- **子 Agent 2**（社媒传播）：写三条不同平台风格的预热文案。
- **子 Agent 3**（PPT 制作）：做大会介绍 PPT。
- 三个 Agent **并行开工**，主 Agent 最后核查汇总，产出完整的"大会启动包"。
- 生成的海报可编辑（换嘉宾名字、调颜色），PPT 含完整议程和品牌信息，文案适配不同平台风格。

