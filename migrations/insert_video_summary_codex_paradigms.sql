## 范式 1：换个思路下指令

**核心转变**：从"怎么解决" → "直接解决"

- **错误方式**：像教AI一样一步步教它怎么做（"先下载视频，再转写英文语音，再翻译成中文字幕..."）
- **正确方式**：直接说目标 —— "下载这个英文视频，生成中文字幕，并硬压进视频里。给我一个可以直接播放的中文字幕视频。不要给教程。"
- Codex 会自己找工具、下载视频、处理音频、生成字幕、翻译、压制。遇到报错会自己换方法继续跑。
- 你只需要关注**目标**和**交付标准**，至于AI怎么去执行，交给它自己想办法。

