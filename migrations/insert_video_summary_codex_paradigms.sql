## 范式 3：重复工作自动化

**核心逻辑**：重复性工作 → Skill + 自动化任务

### 找重复工作的方法
让 Codex 回顾你最近30天的工作记录，找出重复出现、值得封装的手动工作。

### 发票报销案例
- 指令：从 Gmail 找最近30天的发票邮件，下载 PDF 附件，去重，按"开票金额+日期+公司"重命名，最后生成报销 Excel。
- 进一步把这套流程保存成专属 **Skill**（`invoice-reimbursement`），并设置定时任务（每月25号上午10点自动执行）。

### 简历筛选案例
- 每天早上10点自动查邮箱，筛选含简历的邮件，按匹配度分级（超级匹配/值得聊聊/暂待考察），汇总列表，甚至自动给超级匹配的候选人发送约面邮件。
- 原来每天花30分钟看邮件，现在几分钟搞定。

