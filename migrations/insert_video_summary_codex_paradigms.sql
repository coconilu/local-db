## 范式 4：打通各种工具

**核心思路**：让 Codex 成为统一的"指挥中心"和"工具中台"

Codex 有强大的插件库和开源属性，可以连接各类外部工具。

### 飞书文档联动案例
给 Codex 飞书文档链接，它能读取评论，根据评论直接修改文档内容（用不同颜色标注、划线删除原文），实现"评论驱动修改"的自动化机制。

### 飞书多维表格案例
Codex 可以读取多维表格结构，按评论要求批量修改字段类型（如 text → select）、调整数据精度等，全程不用开电脑，手机上就能完成。

### 微信读书联动案例
安装"信读书 Skill"后，Codex 可以读取你的书架、阅读数据、笔记划线，还能做漂亮的 HTML 阅读数据可视化（如年度阅读演化图、阅读人格雷达图）。

### 其他工具
还可以接入 **Remotion**（做动画海报）、**CAD**（做设计图纸）等，把各种工具变成可被 Codex 直接调用的工作流。

