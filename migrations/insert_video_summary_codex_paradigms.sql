## 范式 2：标准化工作外包

**适用场景**：有固定套路的脏活、累活

### 论文排版案例
给 Codex 一份参考文献格式模板，它能自动把混乱的引用格式统一规范，还能按章节结构排版。

### 数据分析案例
- 从聊天记录截图中提取员工信息，生成 Excel 统计表（姓名、工号、部门、礼物），并统计每种礼物采购数量。
- 进一步让 Codex 上网搜索对应商品价格，做采购预算表，包含推荐品牌、预算单价、预算金额、复核链接。
- 全程只需几句指令，输出专业级 Excel。

### 可视化看板案例
给 Codex 原始数据，让它生成 HTML + ECharts 风格的交互式可视化看板，带筛选、图表、趋势分析，10分钟搞定原本需要半天的工作。

