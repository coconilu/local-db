## 提示词使用策略总结

| 阶段 | 核心策略 |
|------|---------|
| **产品定义** | 用 Plan mode 让 AI 主动提问，只需回答「是/否」 |
| **UX 设计** | 先让 AI 调研最佳实践，再输出 Design.md；用 Swarm 并行探索多风格 |
| **原型生成** | 手绘草图 + ChatGPT，从粗糙到专业 |
| **素材生成** | 详细的正面/负面提示词，指定风格、比例、背景 |
| **前端开发** | 基于 PRD 和 Design.md，让 AI 按文档执行 |
| **AI 接入** | 让 AI 自主规划实现方案，遇到 bug 派 AI 工程师修复 |
| **设计微调** | 用精确的设计语言描述修改（字号、间距、颜色、位置） |
