## 一、产品定义阶段（写 PRD）

### 工具：Meo（Plan mode）

**核心策略**：不用自己写 prompt，让 AI 主动提问梳理需求，只需回答「是或否」。

### 产品描述 Prompt

```
我要做的是微信小程序，从一开始就要用微信小程序的技术来构建。

我想创建一款 AI 做饭小程序，名字叫"开饭"。
核心功能是：用户上传买菜订单截图，AI 识别订单里的食材，推荐家常菜，
并生成带步骤的做饭教程。用户可以在手机上跟着做。

具体流程：
1. 用户上传订单截图
2. AI 识别食材
3. 推荐菜品（每次生成 1-3 道菜）
4. 用户选择菜品后进入分步教学流程
5. 每步有倒计时，可以语音播报步骤

技术栈：Taro + React + TypeScript + 微信小程序
```

### Plan mode 下 AI 的提问方向
- 产品定位和目标人群
- 核心功能优先级
- 技术方案选型
- 页面流程和交互细节
- 数据存储方案
- 是否需要用户登录
- 是否需要收藏功能

